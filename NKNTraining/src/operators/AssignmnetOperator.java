package operators;

public class AssignmnetOperator {

	public static void main(String[] args) {
		//Assignmnet =, +=, -=, *=, /=, %=
		int a=10;
		
		a+=5;                                       //a=a+5;
		System.out.println(a);
				
		int b=12;
		b-=5;                                       //b=b-5;
		System.out.println(b);
	
		int c=5;
		c*=5;                                       //c=c*5;
		System.out.println(c);
	
		int d=5;
		d/=5;                                       //d=d/5;
		System.out.println(d);
		
		int e=5;
		e%=5;                                       //e=e%5;
		System.out.println(e);
		}
}
